// Workbook listening tracks: "NN-P.mp3" where NN = lesson, P = part 1..3.
function lessonTracks(from: number, to: number): string[] {
  const tracks: string[] = [];
  for (let lesson = from; lesson <= to; lesson += 1) {
    const nn = String(lesson).padStart(2, '0');
    for (let part = 1; part <= 3; part += 1) tracks.push(`${nn}-${part}.mp3`);
  }
  return tracks;
}

